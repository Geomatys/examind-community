package org.constellation.dto.metadata;

import java.io.Serializable;
import javax.xml.bind.annotation.XmlRootElement;
import org.constellation.dto.Identifiable;

/**
 *
 * @author Guilhem Legal (Geomatys)
 */
@XmlRootElement
public class Attachment extends Identifiable implements Serializable {

    private byte[] content;
    private String uri;
    private String filename;

    public Attachment() {

    }

    public Attachment(Integer id, byte[] content, String uri, String filename) {
        this.content = content;
        this.filename = filename;
        this.id = id;
        this.uri = uri;
    }

    /**
     * @return the content
     */
    public byte[] getContent() {
        return content;
    }

    /**
     * @param content the content to set
     */
    public void setContent(byte[] content) {
        this.content = content;
    }

    /**
     * @return the uri
     */
    public String getUri() {
        return uri;
    }

    /**
     * @param uri the uri to set
     */
    public void setUri(String uri) {
        this.uri = uri;
    }

    /**
     * @return the filename
     */
    public String getFilename() {
        return filename;
    }

    /**
     * @param filename the filename to set
     */
    public void setFilename(String filename) {
        this.filename = filename;
    }
}
